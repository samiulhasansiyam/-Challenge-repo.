#include<bits/stdc++.h>
using namespace std;
int main()
{
    int w;
    cin>>w;
    if(w%2==1)
    {
        cout<<"NO"<< "\n";
    }else
    {
        if(2==w)
        {
            cout<<"No"<< "\n";
        }else
        {
            cout<<"YES"<< "\n";
        }
    }
    return 0;
}